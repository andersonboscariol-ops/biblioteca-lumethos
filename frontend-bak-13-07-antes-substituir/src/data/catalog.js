function cover(title) {
  const words = title.split(' ').filter(w => w.length > 2).slice(0, 3)
  const short = words.join('+') || title.slice(0, 20)
  // Usa gradiente via overlay CSS no frontend
  return `https://placehold.co/300x450/${PALETTE_BG}/${PALETTE_GOLD}?text=${short}`
}

